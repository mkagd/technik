// ========== AKTUALNY POZIOM BEZPIECZE≈ÉSTWA ==========
class SecurityManager {
  
  constructor() {
    // Odczytaj poziom z konfiguracji lub ustaw domy≈õlny
    this.currentLevel = process.env.SECURITY_LEVEL || 'STANDARD';
    this.config = SECURITY_LEVELS[this.currentLevel] || SECURITY_LEVELS.STANDARD;
  }
  
  /**
   * Zwraca aktualnƒÖ konfiguracjƒô bezpiecze≈Ñstwa
   */
  getConfig() {
    return {
      level: this.currentLevel,
      ...this.config
    };
  }
  
  /**
   * Sprawdza czy funkcja jest w≈ÇƒÖczona
   */
  isEnabled(feature) {
    const features = this.config.features;
    
    switch (feature) {
      case 'encryption':
        return features.encryption && features.encryption.enabled;
      
      case 'rateLimiting':
        return features.rateLimiting && features.rateLimiting.enabled;
      
      case 'backup':
        return features.backup && features.backup.enabled;
        
      case 'authentication':
        return features.authentication !== 'none';
        
      default:
        return features[feature] === true;
    }
  }
  
  /**
   * Pobiera ustawienia konkretnej funkcji
   */
  getSettings(feature) {
    return this.config.features[feature];
  }
  
  /**
   * Zmienia poziom bezpiecze≈Ñstwa
   */
  setLevel(newLevel) {
    if (!SECURITY_LEVELS[newLevel]) {
      throw new Error(`Nieznany poziom bezpiecze≈Ñstwa: ${newLevel}`);
    }
    
    this.currentLevel = newLevel;
    this.config = SECURITY_LEVELS[newLevel];
    
    console.log(`üîí Zmieniono poziom bezpiecze≈Ñstwa na: ${this.config.name}`);
    return this.getConfig();
  }
  
  /**
   * Wy≈õwietla aktualnƒÖ konfiguracjƒô
   */
  displayConfig() {
    console.log(`\nüîí AKTUALNY POZIOM BEZPIECZE≈ÉSTWA: ${this.config.name}`);
    console.log(`üìù ${this.config.description}\n`);
    
    const features = this.config.features;
    
    console.log('üîê Szyfrowanie:', features.encryption ? '‚úÖ W≈ÇƒÖczone' : '‚ùå Wy≈ÇƒÖczone');
    if (features.encryption && features.encryption.enabled) {
      console.log(`   ‚îî‚îÄ Pola: ${Array.isArray(features.encryption.fields) ? features.encryption.fields.join(', ') : features.encryption.fields}`);
    }
    
    console.log('üö¶ Rate Limiting:', features.rateLimiting?.enabled ? '‚úÖ W≈ÇƒÖczone' : '‚ùå Wy≈ÇƒÖczone');
    if (features.rateLimiting?.enabled) {
      console.log(`   ‚îî‚îÄ Limit: ${features.rateLimiting.requests} req/${features.rateLimiting.window}`);
    }
    
    console.log('üîê Autoryzacja:', features.authentication || 'Brak');
    console.log('‚è±Ô∏è  Sesja:', features.sessionTimeout || 'Bez limitu');
    console.log('üíæ Backup:', features.backup?.frequency || 'Wy≈ÇƒÖczone');
    
    if (features.bruteForceProtection) {
      console.log('üõ°Ô∏è  Ochrona brute force: ‚úÖ W≈ÇƒÖczona');
    }
    
    if (features.ipWhitelist) {
      console.log('üåê Whitelist IP: ‚úÖ W≈ÇƒÖczona');
    }
    
    if (features.auditLog) {
      console.log('üìã Audit Log: ‚úÖ W≈ÇƒÖczony');
    }
  }
}

// ========== INTEGRACJA Z ISTNIEJƒÑCYMI SYSTEMAMI ==========
class SecurityIntegration {
  
  constructor() {
    this.security = new SecurityManager();
  }
  
  /**
   * Konfiguruje szyfrowanie wed≈Çug poziomu bezpiecze≈Ñstwa
   */
  configureEncryption() {
    const encryptionConfig = this.security.getSettings('encryption');
    
    if (!encryptionConfig || !encryptionConfig.enabled) {
      return null; // Szyfrowanie wy≈ÇƒÖczone
    }
    
    // Zaktualizuj pola do szyfrowania wed≈Çug poziomu
    if (encryptionConfig.fields === 'all') {
      // Wszystkie pola (poziom ENTERPRISE)
      return require('./dataEncryption').SENSITIVE_FIELDS;
    } else if (Array.isArray(encryptionConfig.fields)) {
      // Wybrane pola (poziom STANDARD)
      return {
        clients: encryptionConfig.fields,
        orders: encryptionConfig.fields.filter(f => ['customerName', 'customerPhone', 'customerEmail'].includes(f)),
        employees: encryptionConfig.fields.filter(f => ['firstName', 'lastName', 'email'].includes(f))
      };
    }
    
    return null;
  }
  
  /**
   * Konfiguruje rate limiting wed≈Çug poziomu
   */
  configureRateLimiting() {
    const rateLimitConfig = this.security.getSettings('rateLimiting');
    
    if (!rateLimitConfig || !rateLimitConfig.enabled) {
      return null;
    }
    
    return {
      windowMs: this.parseTimeWindow(rateLimitConfig.window),
      max: rateLimitConfig.requests,
      progressive: rateLimitConfig.progressive || false
    };
  }
  
  /**
   * Konfiguruje backup wed≈Çug poziomu
   */
  configureBackup() {
    const backupConfig = this.security.getSettings('backup');
    
    if (!backupConfig || !backupConfig.enabled) {
      return null;
    }
    
    const schedules = {
      'realtime': '*/5 * * * *',    // Co 5 minut
      'hourly': '0 * * * *',        // Co godzinƒô
      'daily': '0 2 * * *',         // Codziennie o 2:00
      'weekly': '0 2 * * 0'         // W niedzielƒô o 2:00
    };
    
    return {
      schedule: schedules[backupConfig.frequency] || schedules.daily,
      retention: backupConfig.retention || '30d',
      encryption: backupConfig.encryption || false
    };
  }
  
  /**
   * Pomocnicza: parsuje okno czasowe (np. "1m" -> 60000ms)
   */
  parseTimeWindow(window) {
    const units = {
      's': 1000,
      'm': 60 * 1000,
      'h': 60 * 60 * 1000,
      'd': 24 * 60 * 60 * 1000
    };
    
    const match = window.match(/^(\d+)([smhd])$/);
    if (!match) return 60000; // Domy≈õlnie 1 minuta
    
    const [, value, unit] = match;
    return parseInt(value) * (units[unit] || units.m);
  }
}

