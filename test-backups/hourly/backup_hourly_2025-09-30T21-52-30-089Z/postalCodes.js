// Funkcja do wyszukiwania miasta po kodzie pocztowym
export const getCityByPostalCode = (postalCode) => {
  return postalCodeMap[postalCode] || '';
};

// Funkcja do wyszukiwania kodów pocztowych po mieście
export const getPostalCodesByCity = (city) => {
  return Object.keys(postalCodeMap).filter(code => 
    postalCodeMap[code].toLowerCase() === city.toLowerCase()
  );
};

// Lista wszystkich miast w bazie
export const getAllCities = () => {
  return [...new Set(Object.values(postalCodeMap))].sort();
};

// Statystyki bazy
export const getStats = () => {
  const totalCodes = Object.keys(postalCodeMap).length;
  const totalCities = getAllCities().length;
  return {
    totalPostalCodes: totalCodes,
    totalCities: totalCities,
    coverage: 'Województwo podkarpackie i okolice'
  };
};